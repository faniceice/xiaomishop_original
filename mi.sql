SET NAMES 'utf8';
DROP DATABASE IF EXISTS mi;
CREATE DATABASE mi CHARSET=UTF8;
USE mi;

/**小米手机型号归类**/
CREATE TABLE mi_laptop_family(
	fid INT PRIMARY KEY AUTO_INCREMENT,
	fname VARCHAR(32)
);

/**用户列表**/
CREATE TABLE mi_user(
   user_id int(11) PRIMARY KEY  AUTO_INCREMENT,
   user_name varchar(100),
   user_pwd varchar(100)
);

/**手机信息**/
CREATE TABLE mi_laptop (
    lid INT PRIMARY KEY AUTO_INCREMENT,
    family_id INT,    #所属型号
    title VARCHAR(128),
    subtitle VARCHAR(128),
    price DECIMAL(10,2),
    promise VARCHAR(64),
    lname VARCHAR(32),
    memory VARCHAR(32),
    sold_count INT
);

/**小米手机图片**/
CREATE TABLE mi_laptop_pic(
    pid INT PRIMARY KEY AUTO_INCREMENT,
    laptop_id INT,     #手机编号
    sm VARCHAR(128),
    md VARCHAR(128),
    lg VARCHAR(128)
);

/**轮播图片**/
CREATE TABLE mi_index_carousel(
  cid INT PRIMARY KEY AUTO_INCREMENT,
  img VARCHAR(128),
  title VARCHAR(64),
  href VARCHAR(128)
);

/**收货地址信息**/
CREATE TABLE mi_receiver_address(
  aid INT PRIMARY KEY AUTO_INCREMENT,
  uid INT,                #用户编号
  receiver VARCHAR(16),       #接收人姓名
  province VARCHAR(16),       #省
  city VARCHAR(16),           #市
  county VARCHAR(16),         #县
  address VARCHAR(128),       #详细地址
  cellphone VARCHAR(16),      #手机
  fixedphone VARCHAR(16),     #固定电话
  postcode CHAR(6),           #邮编
  tag VARCHAR(16),            #标签名

  is_default BOOLEAN          #是否为当前用户的默认收货地址
);

/**购物车条目**/
CREATE TABLE mi_shoppingcart_item(
	iid INT PRIMARY KEY AUTO_INCREMENT,
	user_id INT,
	product_id INT,
	count INT,
	is_checked BOOLEAN
);

/**用户订单的时间**/
CREATE TABLE mi_order(
  aid INT PRIMARY KEY AUTO_INCREMENT,
  uid INT,
  address_id INT,
  status INT,             #订单状态  1-等待付款  2-等待发货  3-运输中  4-已签收  5-已取消
  order_time BIGINT,      #下单时间
  pay_time BIGINT,        #付款时间
  deliver_time BIGINT,    #发货时间
  received_time BIGINT    #签收时间
)AUTO_INCREMENT=10000000;

/**用户订单的编号**/
CREATE TABLE xz_order_detail(
  did INT PRIMARY KEY AUTO_INCREMENT,
  order_id INT,           #订单编号
  product_id INT,         #产品编号
  count INT               #购买数量
);


